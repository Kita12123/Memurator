//#######################################################################################
//    フォームを出す
//#######################################################################################
$(function() {
    var form = $('.main-form-fadein');   
    form.click(function () {
        $('#main-form').animate({ width: 'toggle' }, 'slow'); // 左右に表示・非表示
    });
});

//#######################################################################################
//    フォームを隠す
//#######################################################################################
$(function() {
    var form = $('#main-form-fadeout');   
    form.click(function () {
        $('#main-form').fadeOut();
    });
});

//#######################################################################################
//    フォームを出す
//#######################################################################################
$(function() {
    var form = $('#main-form-fadein');   
    form.click(function () {
        $('#main-form').fadeIn();
    });
});
